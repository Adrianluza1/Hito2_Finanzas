<script>
export default {
  name: "LoginView",
  data() {
    return {
      email: "",
      password: "",
    };
  },
  methods: {
    handleLogin() {
      if (this.email === "test@example.com" && this.password === "password") {
        this.$router.push("/home");
      } else {
        alert("Credenciales incorrectas. Por favor, inténtalo de nuevo.");
      }
    },
  },
};
</script>

